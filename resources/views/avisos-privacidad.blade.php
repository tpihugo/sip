                        <h2 class="mb-4" style="color: #004777;">Información que se recolecta en este sitio</h2>
                        <p>Los datos que se pueden recopilar en los sitios web de la Universidad de Guadalajara son:</p>

                        <li>Información técnica del equipo con que accede al sitio web. (Dirección IP, navegador que
                            emplea,
