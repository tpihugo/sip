                        <h2 class="mb-4" style="color: #004777;">Tecnología empleada en los sitios web: Cookies</h2>
                        <p>Una cookie es una pequeña pieza de información que es enviadas por el sitio web a su
                            navegador.</p>
                        <p>Las cookies se almacenan en el disco duro de su equipo y se utilizan para determinar sus
                            preferencias cuando se conecta a los servicios de nuestros sitios, así como para
                            rastrear
                            determinados comportamientos o actividades llevadas a cabo por usted dentro de nuestros
                            sitios.</p>

                        <p>Las cookies permiten que los servicios que se ofrecen por el web, puedan adaptar su
                            funcionamiento para que respondan a Usted como un usuario diferente del resto que
                            visitan el
                            sitio web y que esta identificación se refleje en que cada usuario tenga una experiencia
                            personalizada al visitar este sitio.</p>
                        <p>Utilizamos cookies de registro del tráfico para identificar qué páginas se están
                            visitando. Esto
                            nos ayuda a analizar datos sobre el tráfico de la página web y mejorar la calidad de
                            nuestro
                            sitio web para adaptarlo y ajustar la información a las necesidades del cliente. Sólo
                            utilizamos
                            esta información para fines de análisis estadístico, por lo que después de esta acción
                            los datos
                            son eliminados automáticamente del sistema.</p>

                        <p>En general, las cookies nos ayudan a brindarle una mejor experiencia de navegación al
                            momento de
                            visitar nuestro sitio web. Una cookie de ninguna manera nos da acceso a su computadora o
                            a
                            cualquier información sobre usted, excepto los datos que usted decida compartir con
                            nosotros.
                            Como usuario siempre puede elegir entre aceptar o rechazar las cookies. La mayoría de
                            los
                            navegadores web están configurados para aceptar automáticamente las cookies, pero cada
                            usuario
                            puede modificar este permiso en el explorador para rechazar las cookies si así él lo
                            prefiere,
                            aunque esto puede afectar la manera en que funciona este sitio web.</p>
                        <br>
                        <br>

